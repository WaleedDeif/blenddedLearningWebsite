	<li>يفسر اختلاف درجة حرارة الخصيتين عن بقية الجسم.</li>
	<li>يسمي العضو المسؤول عن انتاج الحيوانات المنوية .</li>
<li>	يحدد وظيفة الوعاء الناقل.</li>
<li>	يوضح تركيب الحيوان المنوي.</li>
<li>	يوضح تركيب السائل المنوي.</li>
<li>	يحدد وظيفة السائل المنوي.</li>
<li>	يتوقع ما يحدث لو أن غدة البروستاتا تم استئصالها.</li>
<li>	يحدد مكان تخزين الحيوانات المنوية حتى النضج.</li>
