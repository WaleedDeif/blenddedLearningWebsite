<li>	يحدد مكان حدوث عملية الاخصاب بالجهاز التناسلي الأنثوي.</li>
<li>	يحدد عدد الكروموسومات في خلية اللاقحة.</li>
<li>	يقارن بين مدة بقاء الحيوان المنوي والبويضة في حالة صالحة للإخصاب.</li>
